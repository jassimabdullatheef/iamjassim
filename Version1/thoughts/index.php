            	$("#msg-clear").click(function() {
					$("#msg-success").slideUp();
					$("#usermail-alert").slideUp();
					$("#usermessage-alert").slideUp();
                    $("#useremail").val("");
					$("#usermessage").val("");
                });
				$("#msg-send").click(function() {
                    var useremail = $("#useremail").val();
					var usermsg = $("#usermessage").val();
					
					if($.trim($("#useremail").val()).length == 0){
							$("#usermail-alert").text("Please enter your email id");
							$("#usermail-alert").slideDown();
						}
					else if($.trim($("#usermessage").val()).length == 0){
							$("#usermessage-alert").text("Please enter your message");
							$("#usermessage-alert").slideDown();
						}
					else{ 
						var emailReg = /[-0-9a-zA-Z.+_]+@[-0-9a-zA-Z.+_]+\.[a-zA-Z]{2,4}/;
						if( emailReg.test( useremail ) ) {
							$("#msg-success").html('<span class=" icon-spin5 animate-spin"></span>')
							$("#msg-success").slideDown();
							$.get("sendmail.php", { useremail:$("#useremail").val(), usermsg:$("#usermessage").val() },
								function(data){
									if(data == 1){
										$("#msg-success").html('<span class="icon-thumbs-up-alt" ></span>OK, I received your message, I will get back to you soon..');
										}
									else{
										$("#msg-success").html('<span class="icon-attention" ></span>Oh, Sorry. Couldn\'t send the message. Please choose any other contact option for now. ');
										}
									
								});
							}
						else {
							$("#usermail-alert").text("This is an invalid email id");
							$("#usermail-alert").slideDown();
							}

						}
                });
				$("#useremail").change(function() {
					setTimeout(function(){$("#usermail-alert").slideUp();},1000);
                });
				$("#usermessage").change(function() {
					setTimeout(function(){$("#usermessage-alert").slideUp();},1000);
